-- ============================================================================
-- DESIGN DECISIONS
-- ============================================================================
--
-- 1. Asset ownership model
--    ─────────────────────
--    EBook.id (text, e.g. "ebook-001")
--      → ebook_assets.ebook_id         (entitlement bridge)
--      → ebook_assets.storage_path     (resolved at delivery time)
--      → signed URL (generated by service-role client, never stored)
--
--    order_items.product_id proves purchase entitlement.
--    The download function (Step 4) queries:
--      1. Verify paid order contains product_id = $ebookId, item_type = 'ebook'
--      2. SELECT storage_path FROM ebook_assets
--             WHERE ebook_id = $ebookId AND is_active = true
--      3. storage.from('ebooks').createSignedUrl(storage_path, 3600)
--
-- 2. Versioning
--    ──────────
--    Each row is an immutable version record. Old rows are never deleted;
--    is_active = false marks superseded versions. At most one row per ebook_id
--    may have is_active = true at any given time (enforced by partial unique
--    index). An eBook may temporarily have zero active assets during PDF
--    replacement or after an upload failure — this is intentional and
--    desirable for safe failure recovery. Version numbers are sequential (1, 2…).
--
-- 3. MIME type constraint extensibility
--    ─────────────────────────────────
--    The CHECK constraint currently allows only 'application/pdf'.
--    Adding EPUB later requires one ALTER TABLE migration to drop and
--    redefine the constraint — no table rebuild, no data loss. The
--    storage bucket's allowed_mime_types must also be updated at that time.
--
-- 4. Storage bucket conflict strategy
--    ──────────────────────────────────
--    This migration uses ON CONFLICT DO NOTHING for bucket creation.
--    Rationale: if the bucket already exists in production with different
--    settings, silently overriding those settings could break uploads that
--    are already working. The DO NOTHING approach preserves existing state
--    and a notice block reports which path was taken. If the bucket is new,
--    it is created with the correct private configuration.
--    If the bucket pre-exists with wrong settings, update it manually after
--    inspecting the current configuration in the Supabase dashboard.
--
-- 5. Object path convention
--    ─────────────────────────
--    Canonical path format (generated by the admin uploader in Step 2B):
--      {ebook_id}/v{version}/{sanitized-file-name}.pdf
--    Example:
--      ebook-001/v1/4c-hair-growth-guide.pdf
--
--    Rules enforced by the uploader (not enforceable in SQL):
--      • No customer PII in the path
--      • No order IDs in the path
--      • File name sanitized: lowercase, spaces → hyphens, max 80 chars
--      • Only .pdf extension for MIME type application/pdf
--
-- 6. Checksum
--    ─────────
--    checksum_sha256 is nullable in Step 2A.
--    Step 2B (admin uploader) will compute SHA-256 via the browser's
--    SubtleCrypto API before upload, then store the hex digest here.
--    Supports: duplicate detection, integrity verification, version diffing.
--    No checksum computation occurs in SQL.
-- ============================================================================


