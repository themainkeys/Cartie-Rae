end $$;


-- ─────────────────────────────────────────────────────────────────────────────
-- 1. Private 'ebooks' Storage bucket
--
--    ON CONFLICT DO NOTHING preserves any existing bucket configuration.
--    The notice block reports whether the bucket was newly created or
--    already present. If already present with wrong settings, inspect
--    the Supabase dashboard and update manually.
-- ─────────────────────────────────────────────────────────────────────────────
insert into storage.buckets (id, name, public, file_size_limit, allowed_mime_types)
values (
  'ebooks',
  'ebooks',
  false,             -- PRIVATE: no public read
  52428800,          -- 50 MB per file
  array['application/pdf']
)
on conflict (id) do nothing;

-- Validate effective bucket configuration — abort if insecure or misconfigured.
-- ON CONFLICT DO NOTHING preserves any pre-existing bucket. This block
-- ensures that whatever configuration is now in effect satisfies the required
-- security posture. A public ebooks bucket or unexpected MIME/size settings
-- are hard aborts: they cannot be silently accepted for a private digital-
-- product delivery system.
do $$
declare
  v_public           boolean;
  v_file_size_limit  bigint;
  v_mime_types       text[];
begin
  select public, file_size_limit, allowed_mime_types
  into   v_public, v_file_size_limit, v_mime_types
  from   storage.buckets
  where  id = 'ebooks';

  if not found then
    raise exception
      'The ebooks storage bucket was not created and does not exist. '
      'Investigate storage.buckets permissions.';
  end if;

  raise notice 'ebooks bucket state: public=%, file_size_limit=%, allowed_mime_types=%',
    v_public, v_file_size_limit, v_mime_types;

  if v_public is distinct from false then
    raise exception
      'The ebooks bucket exists but is marked PUBLIC. eBook files must not be '
      'publicly readable. Correct before continuing: '
      'UPDATE storage.buckets SET public = false WHERE id = ''ebooks'';';
  end if;

  if v_file_size_limit is distinct from 52428800 then
    raise exception
      'The ebooks bucket has an unexpected file_size_limit: %. '
      'Expected 52428800 (50 MB). Review and update before continuing.',
      v_file_size_limit;
  end if;

  if v_mime_types is null
     or not ('application/pdf' = any(v_mime_types)) then
    raise exception
      'The ebooks bucket does not permit application/pdf in allowed_mime_types. '
      'Current value: %.', v_mime_types;
  end if;

  raise notice 'ebooks bucket security validation passed.';
end $$;


-- ─────────────────────────────────────────────────────────────────────────────
-- 2. Storage object policies — ebooks bucket
--
--    All policies are scoped to bucket_id = 'ebooks'.
--    Only the active authenticated admin may read, insert, update, or delete.
--    Anonymous users have no access.
--    All other authenticated users have no access.
--    The service-role client (used by Netlify Functions for signed URL
--    generation) bypasses RLS automatically — no policy needed for it.
-- ─────────────────────────────────────────────────────────────────────────────

-- Admin: read object metadata (needed to list uploaded assets in the portal)
drop policy if exists "ebook storage admin select" on storage.objects;
create policy "ebook storage admin select"
  on storage.objects
  for select
  to authenticated
  using (
    bucket_id = 'ebooks'
    and exists (
      select 1
      from   public.admin_users au
      where  au.id        = auth.uid()
        and  au.is_active = true
    )
  );

-- Admin: upload new objects
drop policy if exists "ebook storage admin insert" on storage.objects;
create policy "ebook storage admin insert"
  on storage.objects
  for insert
  to authenticated
  with check (
    bucket_id = 'ebooks'
    and exists (
      select 1
      from   public.admin_users au
      where  au.id        = auth.uid()
        and  au.is_active = true
    )
  );

-- Admin: replace / overwrite objects (needed for re-upload of same path).
-- WITH CHECK is required alongside USING to prevent an update from mutating
-- the object's bucket_id to a different bucket.
drop policy if exists "ebook storage admin update" on storage.objects;
create policy "ebook storage admin update"
  on storage.objects
  for update
  to authenticated
  using (
    bucket_id = 'ebooks'
    and exists (
      select 1
      from   public.admin_users au
      where  au.id        = auth.uid()
        and  au.is_active = true
    )
  )
  with check (
    bucket_id = 'ebooks'
    and exists (
      select 1
      from   public.admin_users au
      where  au.id        = auth.uid()
        and  au.is_active = true
    )
  );

-- Admin: delete obsolete storage objects (e.g. after deactivating an asset)
drop policy if exists "ebook storage admin delete" on storage.objects;
create policy "ebook storage admin delete"
  on storage.objects
  for delete
  to authenticated
  using (
    bucket_id = 'ebooks'
    and exists (
      select 1
      from   public.admin_users au
      where  au.id        = auth.uid()
        and  au.is_active = true
    )
  );


-- ─────────────────────────────────────────────────────────────────────────────
-- 3. ebook_assets table
-- ─────────────────────────────────────────────────────────────────────────────
create table if not exists public.ebook_assets (

  id   uuid primary key default gen_random_uuid(),

  -- ── eBook reference ───────────────────────────────────────────────────────
  -- Matches EBook.id and order_items.product_id (nanoid-style text, not UUID).
  -- Not a FK yet — eBooks are still in localStorage (Step 6 will add the FK
  -- when the ebooks catalog is migrated to Supabase).
  ebook_id text not null
    constraint ebook_assets_ebook_id_nonempty check (ebook_id <> ''),

  -- ── Storage reference ─────────────────────────────────────────────────────
  -- bucket is always 'ebooks'; enforced by check constraint.
  storage_bucket text not null default 'ebooks'
    constraint ebook_assets_bucket_check check (storage_bucket = 'ebooks'),

  -- Path within the ebooks bucket.
  -- Convention: {ebook_id}/v{version}/{sanitized-file-name}.pdf
  -- Generated by the admin uploader; users cannot provide arbitrary paths.
  storage_path text not null
    constraint ebook_assets_path_nonempty check (storage_path <> ''),

  -- ── Versioning ────────────────────────────────────────────────────────────
  -- Sequential per ebook_id (1, 2, 3…). Enforced by unique constraint below.
  version integer not null default 1
    constraint ebook_assets_version_positive check (version > 0),

  -- ── File metadata ─────────────────────────────────────────────────────────
  file_name text not null
    constraint ebook_assets_filename_nonempty check (file_name <> ''),

  -- Currently: application/pdf only.
  -- To add EPUB later: ALTER TABLE DROP CONSTRAINT, ADD CONSTRAINT with new list.
  -- No table rebuild required. Also update storage bucket allowed_mime_types.
  mime_type text not null
    constraint ebook_assets_mime_type_check
      check (mime_type in ('application/pdf')),

  -- Integer bytes; never float. NULL is not permitted.
  size_bytes bigint not null
    constraint ebook_assets_size_nonneg check (size_bytes >= 0),

  -- SHA-256 hex digest — nullable in Step 2A; populated by Step 2B uploader.
  -- Must be a 64-character lowercase hex string when present.
  -- Supports: duplicate detection, integrity verification, version diffing.
  -- Step 2B must store lowercase hex consistently (SubtleCrypto digest → hex).
  checksum_sha256 text
    constraint ebook_assets_checksum_format
      check (
        checksum_sha256 is null
        or checksum_sha256 ~ '^[a-f0-9]{64}$'
      ),

  -- ── Asset state ───────────────────────────────────────────────────────────
  -- Only one row per ebook_id may be active (partial unique index below).
  -- Old rows are retained for history; is_active = false marks them inactive.
  is_active boolean not null default true,

  -- ── Audit ─────────────────────────────────────────────────────────────────
  -- created_by: the Supabase Auth UID of the admin who uploaded this version.
  -- SET NULL on user deletion preserves the asset row for historical record.
  created_by uuid references auth.users(id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);


-- ── Uniqueness constraints ────────────────────────────────────────────────────

-- Each ebook may have version 1, 2, 3… but not two rows with the same version.
alter table public.ebook_assets
  add constraint if not exists ebook_assets_version_unique
    unique (ebook_id, version);

-- Each storage object may be referenced by at most one asset row.
-- Prevents accidental aliasing and aids orphan detection during cleanup.
alter table public.ebook_assets
  add constraint if not exists ebook_assets_storage_path_unique
    unique (storage_path);

-- At most one active asset per eBook at any given time.
-- (Not "exactly one" — an eBook may temporarily have zero active assets
-- during PDF replacement or after upload failure. That is intentional.)
-- Deactivate the current row before inserting a replacement version.
-- Historical rows (is_active = false) are excluded from this index.
create unique index if not exists ebook_assets_one_active_per_ebook
  on public.ebook_assets (ebook_id)
  where is_active = true;

-- RLS
alter table public.ebook_assets enable row level security;


-- ─────────────────────────────────────────────────────────────────────────────
-- 4. Updated-at trigger — table-specific, no shared function touched
-- ─────────────────────────────────────────────────────────────────────────────
create or replace function public.set_ebook_assets_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

drop trigger if exists ebook_assets_set_updated_at on public.ebook_assets;
create trigger ebook_assets_set_updated_at
  before update on public.ebook_assets
  for each row execute function public.set_ebook_assets_updated_at();


-- ─────────────────────────────────────────────────────────────────────────────
-- 5. RLS Policies on ebook_assets
--
--    All policies verify the active-admin condition.
--    Service-role client bypasses RLS automatically.
--    No anonymous access. No general authenticated-user access.
--    No DELETE policy — deactivate rows via is_active = false instead.
--    If an explicit hard-delete is ever needed, add a policy then.
-- ─────────────────────────────────────────────────────────────────────────────

-- Active admin may read all asset rows (needed for portal asset listing)
drop policy if exists "ebook_assets admin select" on public.ebook_assets;
create policy "ebook_assets admin select"
  on public.ebook_assets
  for select
  to authenticated
  using (
    exists (
      select 1
      from   public.admin_users au
      where  au.id        = auth.uid()
        and  au.is_active = true
    )
  );

-- Active admin may insert new asset records
drop policy if exists "ebook_assets admin insert" on public.ebook_assets;
create policy "ebook_assets admin insert"
  on public.ebook_assets
  for insert
  to authenticated
  with check (
    exists (
      select 1
      from   public.admin_users au
      where  au.id        = auth.uid()
        and  au.is_active = true
    )
  );

-- Active admin may update asset records (e.g. set is_active = false, add checksum)
-- USING: must be an active admin to target the row
-- WITH CHECK: must remain an active admin after the update
drop policy if exists "ebook_assets admin update" on public.ebook_assets;
create policy "ebook_assets admin update"
  on public.ebook_assets
  for update
  to authenticated
  using (
    exists (
      select 1
      from   public.admin_users au
      where  au.id        = auth.uid()
        and  au.is_active = true
    )
  )
  with check (
    exists (
      select 1
      from   public.admin_users au
      where  au.id        = auth.uid()
        and  au.is_active = true
    )
  );


-- ─────────────────────────────────────────────────────────────────────────────
-- 6. Indexes
-- ─────────────────────────────────────────────────────────────────────────────

-- Primary lookup: all assets for a given ebook (version history list)
create index if not exists ebook_assets_ebook_id_idx
  on public.ebook_assets (ebook_id);

-- Admin portal ordering: most recently uploaded first
create index if not exists ebook_assets_created_at_idx
  on public.ebook_assets (created_at desc);

-- Integrity: look up an asset by its storage path (e.g. to detect orphaned objects).
-- Also serves lookups by storage_path for the unique constraint above.
create index if not exists ebook_assets_storage_path_idx
  on public.ebook_assets (storage_path);

-- Notes on indexes not added:
--   is_active alone: a Boolean index over a mostly-true column is low-value
--     and provides no selectivity improvement. The partial unique index
--     ebook_assets_one_active_per_ebook already covers the principal
--     active-asset lookup (WHERE ebook_id = $x AND is_active = true).
--   (ebook_id, is_active): redundant with the partial unique index above.


